function f = funct(x)
    A = zeros(10,4);
    for i = 1:4
        A(1,i) = 4.0;
        A(2,i) = 1.0;
        A(3,i) = 8.0;
        A(4,i) = 6.0;
    end
    for i = 1:2
        A(5,2*(i-1)+1)  = 3.0;
        A(5,2*i)        = 7.0;
        A(6,2*(i-1)+1)  = 2.0;
        A(6,2*i)        = 9.0;
        A(7,i)          = 5.0;
        A(7,i+2)        = 3.0;
        A(8,2*(i-1)+1)  = 8.0;
        A(8,2*i)        = 1.0;
        A(9,2*(i-1)+1)  = 6.0;
        A(9,2*i)        = 2.0;
        A(10,2*(i-1)+1) = 7.0;
        A(10,2*i)       = 3.6;
    end
    C = zeros(10,1);
    C(1) = 0.1;
    C(2) = 0.2;
    C(3) = 0.2;
    C(4) = 0.4;
    C(5) = 0.4;
    C(6) = 0.6;
    C(7) = 0.3;
    C(8) = 0.7;
    C(9) = 0.5;
    C(10)= 0.5;

    f  = 0.0;
    FA = 0.0;

    for i = 1:10
        for j = 1:4
            FA = FA +(x(j)-A(i,j))^2;
        end
        if ((FA+C(i)) == 0.0)
            f = 1.e+25;
            return
        end
        f  = f -1.0/(FA+C(i));
        FA = 0.0;
    end
    return
end